<h1>Join Active 20-30 of Portland #122</h1>
<h2>How to Join</h2>
<p>To join Active 20-30, you must:</p>
<ul>
    <li>Be between the ages of 20 and 39.</li>
    <li>Attend at least one volunteer event.</li>
    <li>Attend a board meeting. Mettings are every third Thursday of each month @ 8 pm.</li>
    <li>Full out the Membership Application and turn it in to the President.</li>
    <li>Pay club dues.</li>
</ul>
<h2>FAQs</h2>
<dl>
    <dt>What is the Active 20-30 Club of Portland?</dt>
    <dd>
        <p>The Active 20-30 Club of Portland is a group of young professionals who are looking to better their community by assisting the needs of children.  The primary reason for our group is philanthropy â€“ it doesn't mean we don't have some fun and network in the process!</p>
    </dd>
    <dt>How many members are in the club?</dt>
    <dd>
        <p>We are just coming off our first year as a club.  As of September 2014, we have 10 active members and looking to grow our membership!  The more members, the more we can do for the kiddos.</p>
    </dd>
    <dt>Is there an age requirement?</dt>
    <dd>
        <p>Yes.  The Active 20-30 Club is limited to those ages 20-39.  For those who age-out of the club, we offer annual past-active socials each year which encourage life-long involvement.</p>
    </dd>
    <dt>How do I become a member?</dt>
    <dd>
        <p>We are consistently recruiting new members and we encourage interested parties to contact us regarding upcoming events and membership applications.</p>
    </dd>
    <dt>How often does the club meet?</dt>
    <dd>
        <p>The club has one General meeting and one Board meeting, typically held back to back the third Thursday of each month beginning at 7pm.  The location may change, so please click here for additional details. (Link to Calendar of Mtg/Events) We review past events, receive committee updates, prepare for upcoming opportunities, and spend a bit of time socializing as well!</p>
        <p>Along with 'business meetings' we try to gather at least once each month for a service project and another time for purely social reasons such as trivia night or movies in the park.  This offers members a great balance between hard work and having fun.</p>
    </dd>
    <dt>What are the financial commitments?</dt>
    <dd>
        <p>The Club has annual dues which are set the by Club's Board of Directors each year.  These dues cover our national dues as well.  Dues are payable annually, quarterly, or monthly (depending on how you want to handle it).  Presently, our dues are set at $191annually and can be pro-rated based on when you join. Remember that many employers are willing to pay philanthropic membership dues, so don't forget to ask!</p>
    </dd>
    <dt>What are the time commitments?</dt>
    <dd>
        <p>There are no formal time commitments, but we do like to see our members at our meetings and projects throughout the year.  Our goal is to provide plenty of opportunity at different times to accommodate different schedules.  We figure you'll get as much out of the club as you put in.  In addition, all of our members are expected to attend and help with the annual events like the Back to School Celebration and the Garage Sale Fundraiser.</p>
    </dd>
